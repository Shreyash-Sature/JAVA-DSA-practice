/*
Arithmatic Operators : 
Binary : +, -, *, /, % (performs operation on 2 operands eg 'a+b')
Unary : ++(increment operator), --(decrement operator)  (a=a+1  => a++)

*/

public class B_Arithmatic_Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;
        System.out.println(b);
        
    }
    
}
